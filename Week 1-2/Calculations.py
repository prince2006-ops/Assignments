# finding the sum,difference,product,division,modulus and floor division of 2 numbers

first_number=float(input("Enter the first number:"))
second_number=float(input("Enter the second number:"))
add=first_number+second_number
sub=first_number-second_number
mul=first_number*second_number
div=first_number/second_number
mod=first_number%second_number
floor_division= first_number//second_number
print(f"The sum is {add}")
print(f"The difference is {sub}")
print(f"The product is {mul}")
print(f"The division is {div}")
print(f"The mod is {mod}")
print(f"The floor division is {floor_division}")




